CREATE TABLE Assets(
	ItemId INT IDENTITY(1001, 1) PRIMARY KEY,
  GenericName VARCHAR(255) NOT NULL,
  DeptCode VARCHAR(10) NOT NULL,
  CategoryCode VARCHAR(10) NOT NULL,
  UnitCost DECIMAL(11, 2) DEFAULT NULL,
  NewUnitCost DECIMAL(11, 2) DEFAULT NULL,
  SetNo INT DEFAULT NULL,
  DateReceived DATE DEFAULT NULL,
  SupplierName VARCHAR(255) DEFAULT NULL,
  PurchaseOrderNo VARCHAR(255) DEFAULT NULL,
  InvoiceNo VARCHAR(255) DEFAULT NULL,
  BrandName VARCHAR(255) DEFAULT NULL,
  BrandModel VARCHAR(255) DEFAULT NULL,
  SerialNo VARCHAR(255) DEFAULT NULL,
  ItemSpecs VARCHAR(1000) DEFAULT NULL,
  NewAssetCode VARCHAR(255) DEFAULT NULL,
  AssetTagStatus BIT DEFAULT 0,
  ItemRemarks VARCHAR(255) DEFAULT NULL,
  OldAssetCode VARCHAR(255) DEFAULT NULL,
  PhysicalLocation VARCHAR(255) DEFAULT NULL,
  ItemCode VARCHAR(20) DEFAULT NULL,
  ReceivingReport VARCHAR(255) DEFAULT NULL,
  Condemned BIT DEFAULT 0,
  Salvaged BIT DEFAULT 0,
  Deleted BIT DEFAULT 0
)
